    <div class="col-6 Bio2">

<p>
    En 1965, lui et son coéquipier Elgin Baylor deviennent les deux premiers joueurs à atteindre 2 000 points en une saison. Après le départ de celui-ci, il songe également à s'arrêter. Mais l'équipe est expérimentée et plus homogène. Les Lakers réalisent alors une saison 1971-1972 impressionnante avec 69 victoires (dont 33 consécutives), record qui ne sera battu que par les Bulls de Chicago de Michael Jordan en 1996. Le titre tant désiré est atteint contre les Knicks de New York. La saison suivante, ses statistiques baissent et il perd sa dernière chance de titre contre des Knicks revanchards. Il termine sa carrière l'année suivante.

Il est élu au Naismith Memorial Hall Of Fame en 1980 et son maillot, le no 44 est retiré par les Lakers de Los Angeles.
</p>

<br>

<p>
Ce génie du basket dont Kobe Bryant en personne a déclaré qu'il était le meilleur joueur de l'histoire des Lakers demeure l'un des personnages les plus célèbres de la planète tout en étant parfois inconnu. En effet c'est sa silhouette qui est utilisée pour apparaître sur le logo de la NBA.

En outre, il est le principal artisan du succès des Lakers dans les années 2000, en tant que Général Manager c'est lui qui signe Shaquille O'Neal alors free-agent en 1996 et recrute Kobe Bryant (drafté en 13e position) la même année dans un échange avec les Hornets de Charlotte contre Vlade Divac. Ce transfert est à juste titre considéré aujourd'hui comme le transfert le plus rentable de tous les temps.
</p>
