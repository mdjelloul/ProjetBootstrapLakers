            <h1>Jerry West</h1>
            <br>

    <p>
        Jerry Alan West, surnommé Mr. Clutch, né le 28 mai 1938 à Cheylan, en Virginie-Occidentale, est un joueur de basket-ball américain qui évoluait au poste de meneur mais aussi d'arrière. Il fut l'un des premiers joueurs à être qualifié de combo guard.
    </p>

        <br>
    <p>
       Champion olympique en 1960 avec l'équipe des États-Unis, et champion NBA 1972 avec les Lakers de Los Angeles, il remporte le titre de MVP des Finales NBA en 1969 et termine à dix reprises dans le All-NBA First Team, premier cinq NBA. En tant que dirigeant, il remporte six titres NBA avec les Lakers, et termine à deux reprises meilleur dirigeant de l'année.
    </p>
    
