<p>
    Kareem Abdul-Jabbar rejoint une franchise qui vient de terminer une saison 1974-1975 sans disputer les séries éliminatoires, ce qui est la première fois depuis la saison 1957-1958. Malgré une bonne saison de Kareem Abdul-Jabbar, qui remporte son quatrième titre de Most Valuable Player, devant Bob McAdoo, et figure dans le premier cinq de la ligue et le deuxième cinq défensif, la franchise de Los Angeles échoue de nouveau dans sa tentative de rejoindre les séries éliminatoires, terminant à la quatrième place de la division Pacifique, et au sixième rang de la conférence Ouest. Le nouveau pivot des Lakers termine deuxième marqueur avec une moyenne de 27,7 points, premier rebondeur avec 16,9, statistique où il est obtient le plus grand total sur la saison avec 1 383, tout comme pour les rebonds défensifs. Il occupe également la première place de la statistique du contre avec 4,153.
</p>

<br>

<p>
    Les Lakers qui ambitionnent de remporter un troisième titre consécutif, le Three-peat, terminent la saison régulière 1988-1989 avec un bilan de cinquante-sept victoires et vingt-cinq défaites, obtenant la première place de conférence Ouest. Kareem Abdul-Jabbar, qui a annoncé sa retraite en début de saison, termine celle-ci avec des statistiques de 10,1 points, 4,5 rebonds, 1 passe, 1,1 contre, terminant cinquième meilleur marqueur et rebondeur de sa franchise, et meilleur contreur99. Les Lakers débutent ses playoffs par trois sweep - face aux Trail Blazzers de Portland, Supersonics de Seattle et Suns de Phoenix, ce qu'aucune franchise NBA n'a réussi jusqu'alors dans l'histoire de la NBA. Privé de Byron Scott, blessé pour l'ensemble de la série des Finales contre les Detroit Pistons, puis de Magic Johnson blessé lors du deuxième match - il disputera 5 minutes lors du troisième match - Kareem Abdul-Jabbar doit de nouveau assumer un rôle important en attaque, inscrivant ainsi 24 points et captant 13 rebonds lors du troisième match. Il dispute son dernier match le 13 juin 1989 au Forum d'Inglewood, lors du quatrième et dernier match de la série. Il réussit 7 points, 4 rebonds, 3 passes en 29 minutes et quitte le parquet à treize secondes du terme sous une standing ovation du public et du banc adverse Les Detroit Pistons remportent cette Finale par 4 à 0.
</p>
