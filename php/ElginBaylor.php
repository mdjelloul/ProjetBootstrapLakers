            <h1>Elgin Baylor</h1>
            <br>

    <p>
        Elgin Gay Baylor, né le 16 septembre 1934 à Washington DC aux États-Unis et mort le 22 mars 2021 à Los Angeles, est un joueur de basket-ball américain.

Cet ailier a joué 13 saisons en NBA pour Lakers de Los Angeles. De lui, Bill Russell dit qu'il est le point de repère de la perfection[réf. nécessaire]. Avec 8 finales perdues, il est le joueur qui a disputé le plus de finales NBA sans remporter le titre.
    
    </p>
    
