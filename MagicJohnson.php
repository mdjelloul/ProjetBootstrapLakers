            <h1>Earvin Johnson</h1>
            <br>

    <p>
        Earvin Johnson Jr., dit Magic Johnson, né le 14 août 1959 à Lansing dans le Michigan est un joueur américain de basket-ball.

        Il devient champion universitaire avec les Spartans de l'université d'État du Michigan lors de l'édition de 1979, édition où il est désigné joueur le plus remarquable ou Most Outstanding Player. Il rejoint ensuite la National Basketball Association (NBA) et la franchise des Lakers de Los Angeles où il remporte cinq titres de champion, en 1980, 1982, 1985, 1987 et 1988.
    </p>
    
