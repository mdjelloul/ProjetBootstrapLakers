<p>
    Avant l'annonce de sa séropositivité, il est sélectionné en septembre 1991 parmi les dix joueurs retenus pour défendre les couleurs américaines au sein de la Dream Team lors des Jeux olympiques de Barcelone en 1992133. Il s'avère également essentiel pour convaincre Michael Jordan et Larry Bird de faire partie de cette équipe133. Sa présence est critiquée par certains : l'Australie, et en particulier le pivot Ray Borner, déclare ne pas vouloir rencontrer la sélection américaine si Magic fait partie de l'effectif134. Des doutes, anonymes, proviennent également de l'effectif même de la Dream Team135. Toutefois, ni le comité de sélection, ni les médecins, ne lui interdisent de jouer136. Cette sélection, également composée de Michael Jordan et Scottie Pippen des Bulls de Chicago, Karl Malone et John Stockton du Jazz de l'Utah, Charles Barkley des Sixers de Philadelphie, Larry Bird des Celtics de Boston, Patrick Ewing des Knicks de New York, Chris Mullin des Warriors de Golden State et David Robinson des Spurs de San Antonio, puis plus tard de Clyde Drexler des Trail Blazers de Portland et de Christian Laettner, champion de NCAA avec les Blue Devils de Duke, remporte tout d'abord le tournoi des Amériques, avec six victoires en six rencontres, avant de se rendre en Europe pour disputer les Jeux. Durant cette compétition, il inscrit 9,7 points de moyenne et délivre 54 passes décisives au total, terminant meilleur passeur de l'équipe137. Magic Johnson est l'attraction principale de la cérémonie d'ouverture. Comme attendu, la sélection américaine domine la compétition, remportant l'ensemble de ses rencontres avec un écart moyen de 43,8 points. La domination est telle que l'entraîneur Chuck Daly se permet même le luxe de ne prendre aucun temps-mort sur l'ensemble du tournoi olympique. Les statistiques de Magic Johnson sont de 8 points, 2,3 rebonds de moyenne, et 33 passes décisives et 8 interceptions sur la compétition.
</p>

