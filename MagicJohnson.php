            <p>
                Le premier match de Magic Johnson, contre les Clippers de San Diego, reste toujours dans la mémoire de Kareem Abdul-Jabbar, son coéquipier et vedette des Lakers : celui-ci, après une passe de Magic Johnson, réussit un skyhook (bras roulé) à la sonnerie finale appelé buzzer-beater en NBA et donne la victoire à son équipe. Magic Johnson lui saute alors dans les bras, comme s'ils avaient remporté le titre NBA. Abdul-Jabbar lui dit alors qu'il reste quatre-vingt-un matchs de saison régulière33. En janvier, il est désigné pour figurer dans le cinq de départ de l'équipe de la conférence Ouest lors du NBA all-Star Game, devenant le premier rookieNotes 4 depuis Elvin Hayes en 1969 à obtenir un tel honneur
